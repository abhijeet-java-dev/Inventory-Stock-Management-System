    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
     String userid=cbEmpId4.getSelectedItem().toString();
     try
     {
     boolean result=ReceptionistDAO.deleteReceptionist(userid);
     if(result)
     {   
     JOptionPane.showMessageDialog(null,"Receptionist Delete Successfully !!","Successfully",JOptionPane.INFORMATION_MESSAGE);
      cbEmpId4.removeItemAt(cbEmpId4.getSelectedIndex());
      if(cbEmpId4.getItemCount()==0)
          btnRemove.setEnabled(false);
      return;
     }
      JOptionPane.showMessageDialog(null,"Receptionist Not Delete Successfully !!","Error",JOptionPane.INFORMATION_MESSAGE);
     }catch(SQLException e1)
     {
        JOptionPane.showMessageDialog(this,"DataBase Error ! ","Error ",JOptionPane.ERROR_MESSAGE);  
        e1.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

