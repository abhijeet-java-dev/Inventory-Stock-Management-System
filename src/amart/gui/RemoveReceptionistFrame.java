    private void getReceptionists() {
        try{
       cbEmpId4.removeAllItems();
       Set<String>  recepId=ReceptionistDAO.getAllReceptionistId();
       if(recepId.isEmpty())
       {
       JOptionPane.showMessageDialog(null,"No Receptionist Present ","Receptionist",JOptionPane.INFORMATION_MESSAGE);
       return;
       }    
       btnRemove.setEnabled(true);
       for(String id:recepId)
       {
         cbEmpId4.addItem(id);
       } 
       }catch(SQLException e1)
     {
        JOptionPane.showMessageDialog(this,"DataBase Error ! ","Error ",JOptionPane.ERROR_MESSAGE);  
        e1.printStackTrace();
        }
    }
